import mongoose from "mongoose";

const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    default: "standard",
  },
});

const EyelashSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
});

const UserModel = mongoose.model("User", UserSchema);
const EyelashModel = mongoose.model("Eyelash", EyelashSchema);

export { UserModel, EyelashModel };
